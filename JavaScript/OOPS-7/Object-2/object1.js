/*
Javascript Object:
            Javacsript object is an entity having properties and methods.
            Ex: pen, body, animal etc.
            Javacsript is a object based language.
            Everything is an object in javascript.
            Here, we dont create class to get object. But, we directly create objects.

Creating objects:
            Three ways to create objects:
            By object literal
            By creating instance of object(using new keyword)
            By using object constructor(using new keyword)
*/
/*
By object literal:
syntax:
            object = {property1:value1, property2:value2,..., propertyN:valueN}
*/
emp = {id:103, name:"Rajoli", salary:45000};
console.log(emp.id);
console.log(emp.name);
console.log(emp.salary);