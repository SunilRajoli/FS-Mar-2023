/*
JavaScript Comments:
            Javascripts comments are meaningful way to deliver messages.
            Javascript comments are ignored by the javascript engine i.e, embedded in browser.

Advantages:
            To make code easy to understand.
            To avoid unnecessary code.

Types of Comments:
    There are two types of comments:
            Single line comment
            Multiline comment
*/
//Single line comment: Represented by double forward slashes(//)
var a = 10;
var b = 20;
var c = a + b; //It adds value of a and b
console.log(c); //print sum of c

//Multiline comments: Represented by forward slash asterisk and asterisk with forward slash(/**/)
/*
It is Multiline comment
It will not be displayed
*/
console.log("It is Multiline comment");